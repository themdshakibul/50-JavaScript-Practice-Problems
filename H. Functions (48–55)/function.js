// 49. even/odd চেক করার function লেখো
//! Ans:
function numberOfEvenOdd(number) {
  for (let i = 1; i < number; i++) {
    if (number % 2 === 0) {
      return "Even Numbers";
    } else {
      return "Odd Numbers";
    }
  }
}

console.log(numberOfEvenOdd(20));

